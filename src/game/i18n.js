function resolveMessage(language, key) {
  return TRANSLATIONS[language]?.[key] ?? TRANSLATIONS[DEFAULT_LANGUAGE][key] ?? key;
}

export function createI18n(language = DEFAULT_LANGUAGE) {
  const activeLanguage = TRANSLATIONS[language] ? language : DEFAULT_LANGUAGE;
  const separators = {
    en: { comma: ', ', semicolon: '; ' },
    fr: { comma: ', ', semicolon: ' ; ' },
    zh: { comma: '，', semicolon: '；' },
  }[activeLanguage];

  function t(key, params = {}) {
    const message = resolveMessage(activeLanguage, key);
    return typeof message === 'function' ? message(params) : message;
  }

  return {
    language: activeLanguage,
    options: LANGUAGE_OPTIONS,
    t,
    join(items, style = 'comma') {
      return items.filter(Boolean).join(separators[style] ?? separators.comma);
    },
    playerLabel(player) {
      return t(PLAYER_KEYS[player] ?? player);
    },
    moveTypeLabel(moveType) {
      return t(MOVE_TYPE_KEYS[moveType] ?? moveType);
    },
    nodeLabel(label) {
      return Number.isInteger(label) ? t('node.numbered', { label }) : t('node.plain');
    },
    pieceLabel(digit) {
      return t('piece.label', { digit });
    },
  };
}
